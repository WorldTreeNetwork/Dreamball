    \\//
    \\// Minimal dCBOR decoder. Matches the subset emitted by the Zig
    \\// encoder in src/envelope.zig: tags 200 (envelope) / 201 (leaf) /
    \\// 1 (epoch time), canonical map-key ordering, smallest-int form,
