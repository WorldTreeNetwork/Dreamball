pub fn main() !void {
    var gpa_state: std.heap.DebugAllocator(.{}) = .init;
    defer _ = gpa_state.deinit();
    const gpa = gpa_state.allocator();

    const io = std.Io.Threaded.global_single_threaded.io();

    // Ensure src/lib/generated/ exists.
    std.Io.Dir.cwd().createDirPath(io, "src/lib/generated") catch |e| switch (e) {
        error.PathAlreadyExists => {},
        else => return e,
    };

    try writeFile(TYPES_OUT, TYPES_SRC);
    try writeFile(CBOR_OUT, CBOR_SRC);
    try writeFile(README_OUT, README_SRC);

    _ = gpa;
    const stdout = std.Io.File.stdout();
    var buf: [256]u8 = undefined;
    var w = stdout.writer(io, &buf);
    try w.interface.print(
        "schema-gen wrote: {s}, {s}, {s}\n",
        .{ TYPES_OUT, CBOR_OUT, README_OUT },
    );
    try w.interface.flush();
}

fn writeFile(path: []const u8, contents: []const u8) !void {
    const io = std.Io.Threaded.global_single_threaded.io();
    var file = try std.Io.Dir.cwd().createFile(io, path, .{ .truncate = true });
    defer file.close(io);
    var buf: [4096]u8 = undefined;
    var w = file.writer(io, &buf);
    try w.interface.writeAll(contents);
    try w.interface.flush();
}
